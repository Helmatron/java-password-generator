package org.lessons.java.security;

public class Utente {

	public String nome;
	public String cognome;
	public String colore;
	public int giorno;
	public int mese;
	public int anno;

	public Utente(String nome, String cognome, String colore, int giorno, int mese, int anno) {
		this.nome = nome;
		this.cognome = cognome;
		this.colore = colore;
		this.giorno = giorno;
		this.mese = mese;
		this.anno = anno;

	}

}
